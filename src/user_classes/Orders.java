package user_classes;

/**
 *
 * @author hp
 */
public class Orders {
    int foodID;
    int amount;
    double price;
    public Orders(int foodID, int amount){
        this.foodID = foodID;
        this.amount = amount;
    }

    public int getFoodID() {
        return foodID;
    }

    public void setFoodID(int foodID) {
        this.foodID = foodID;
    }

    public double getPrice() {
        return price;
    }

    public void setPrice(double price) {
        this.price = price;
    }

    public int getAmount() {
        return amount;
    }

    public void setAmount(int amount) {
        this.amount = amount;
    }
}
